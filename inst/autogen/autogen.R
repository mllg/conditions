conditions = c(
  "assertion",
  "deprecated",
  "dimension",
  "future",
  "index",
  "io",
  "length",
  "library",
  "lookup",
  "missing",
  "name",
  "runtime",
  "type",
  "value"
)

template = trimws("
#' @rdname std_conditions
#' @useDynLib conditions condition_%1$s_
#' @export
%2$s_%1$s = function(message, call = sys.call(-1L)) {
  .Call(condition_%1$s_, \"%2$s\", message, call)
}")

pkg = devtools::as.package(".")
con = file(file.path(pkg$path, "R", "autogenerated.R"), "wt")
cat("### This file is automatically generated.", "### See [conditions]/inst/autogen/autogen.R.", "", sep = "\n", file = con)
for (type in c("error", "warning", "message")) {
  for (cond in conditions) {
    cat(sprintf(template, type, cond), "\n\n", sep = "", file = con)
  }
}
close(con)
